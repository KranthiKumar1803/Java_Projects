package SnippetCodes;

public class Developers {

    private String name;
    private int Id;
    private String salary;

    public Developers(String name, int id, String salary) {
        this.name = name;
        Id = id;
        this.salary = salary;
    }


    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }


}
